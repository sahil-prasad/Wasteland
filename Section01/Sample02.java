public class Sample02 {
    public static void main(String[]args){
        boolean var;
        var=true;//boolean value can't contain anything other than true or false
        System.out.println(var);

        char var2='&';//string of one character enclosed under single quote
        System.out.println(var2);//unicode character also runs
        char var3='\u00A7';//unicode character
        System.out.println(var3);
    }
    
}
